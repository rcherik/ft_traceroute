#include <stdlib.h>

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	f;

	i = 0;
	f = (char)c;
	while (*s)
	{
		i++;
		s++;
	}
	i++;
	while (i != 0)
	{
		if (*s == f)
			return ((char *)s);
		s--;
		i--;
	}
	return (NULL);
}
