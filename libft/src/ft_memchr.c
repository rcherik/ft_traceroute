#include <stdlib.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*str;
	char		f;

	str = s;
	f = (char)c;
	while (n-- != 0)
	{
		if (*str == f)
			return ((void *)str);
		str++;
	}
	return (NULL);
}
