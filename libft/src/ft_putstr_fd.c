#include <unistd.h>

void	ft_putstr_fd(char const *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
	{
		write(fd, "(null)", 6);
		return ;
	}
	while (s[i])
		i++;
	write(fd, s, i);
}
